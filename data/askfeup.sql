DROP TABLE IF EXISTS Utizador;
CREATE TABLE Utilizador (
	idUtilizador INTEGER PRIMARY KEY AUTOINCREMENT,
	username VARCHAR2(100),
	password VARCHAR2(255),
	ultimoLogin DATE,
	registo DATE,
	sexo VARCHAR2(15),
	imagem VARCHAR2(255)
);

DROP TABLE IF EXISTS Pergunta;
CREATE TABLE Pergunta (
	idPergunta INTEGER PRIMARY KEY AUTOINCREMENT,
	idUtilizador INTEGER NOT NULL,
	texto VARCHAR2(100),
	descricao VARCHAR2(255),
	idCategory INTEGER NOT NULL
);

DROP TABLE IF EXISTS Resposta;
CREATE TABLE Resposta (
	idResposta INTEGER PRIMARY KEY AUTOINCREMENT,
	idUtilizador INTEGER NOT NULL,
	idPergunta INTEGER NOT NULL,
	descricao VARCHAR2(255)
);

DROP TABLE IF EXISTS Voto;
CREATE TABLE Voto (
	idVoto INTEGER PRIMARY KEY AUTOINCREMENT,
	id INTEGER NOT NULL,
	question VARCHAR2(255) NOT NULL
);

DROP TABLE IF EXISTS Categoria;
CREATE TABLE Categoria (
	idCategoria INTEGER PRIMARY KEY AUTOINCREMENT,
	categoria VARCHAR2(100)
);